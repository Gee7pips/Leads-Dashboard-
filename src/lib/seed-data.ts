export function getFallbackStats() {
  const leads = SEED_LEADS

  const totalLeads = leads.length
  const hotLeads = leads.filter((l) => l.hotLead).length
  const dentalLeads = leads.filter((l) => l.sector === "Dental").length

  const activeLeads = leads.filter(
    (l) => l.status === "active" && l.stage !== "won" && l.stage !== "lost"
  )
  const pipelineValue = activeLeads.reduce((sum, l) => sum + (l.estimatedValue || 0), 0)

  const wonLeads = leads.filter((l) => l.stage === "won").length
  const conversionRate = totalLeads > 0 ? Math.round((wonLeads / totalLeads) * 100) : 0

  // By sector
  const sectorMap: Record<string, number> = {}
  leads.forEach((l) => {
    sectorMap[l.sector] = (sectorMap[l.sector] || 0) + 1
  })
  const bySector = Object.entries(sectorMap)
    .map(([sector, count]) => ({ sector, count }))
    .sort((a, b) => b.count - a.count)

  // By tier
  const tierMap: Record<string, number> = {}
  leads.forEach((l) => {
    const key = `Tier ${l.tier}`
    tierMap[key] = (tierMap[key] || 0) + 1
  })
  const byTier = [
    { tier: "Tier 1", count: tierMap["Tier 1"] || 0 },
    { tier: "Tier 2", count: tierMap["Tier 2"] || 0 },
    { tier: "Tier 3", count: tierMap["Tier 3"] || 0 },
  ]

  // By stage
  const stages = [
    "new",
    "contacted",
    "demo_sent",
    "meeting_booked",
    "proposal_sent",
    "negotiation",
    "won",
    "lost",
  ]
  const stageLabels: Record<string, string> = {
    new: "New",
    contacted: "Contacted",
    demo_sent: "Demo Sent",
    meeting_booked: "Meeting Booked",
    proposal_sent: "Proposal Sent",
    negotiation: "Negotiation",
    won: "Won",
    lost: "Lost",
  }
  const byStage = stages.map((stage) => ({
    stage,
    label: stageLabels[stage],
    count: leads.filter((l) => l.stage === stage).length,
  }))

  // Recent activities with lead info (matches Prisma include shape)
  const recentActivities = SEED_ACTIVITIES
    .map((act) => ({
      ...act,
      lead: SEED_LEADS.find((l) => l.id === act.leadId)
        ? { id: act.leadId, name: SEED_LEADS.find((l) => l.id === act.leadId)!.name }
        : { id: act.leadId, name: "Unknown" },
    }))
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
    .slice(0, 10)

  return {
    totalLeads,
    hotLeads,
    dentalLeads,
    pipelineValue,
    conversionRate,
    bySector,
    byTier,
    byStage,
    recentActivities,
    activePipeline: activeLeads.length,
    wonLeads,
    lostLeads: leads.filter((l) => l.stage === "lost").length,
  }
}

export function getFallbackLeads(filters: LeadFilters = {}): LeadRecord[] {
  let leads = [...SEED_LEADS]

  if (filters.sector) {
    leads = leads.filter((l) => l.sector === filters.sector)
  }
  if (filters.tier) {
    const tierNum = parseInt(filters.tier)
    if (!isNaN(tierNum)) leads = leads.filter((l) => l.tier === tierNum)
  }
  if (filters.stage) {
    leads = leads.filter((l) => l.stage === filters.stage)
  }
  if (filters.status) {
    leads = leads.filter((l) => l.status === filters.status)
  }
  if (filters.hotLead === "true") {
    leads = leads.filter((l) => l.hotLead === true)
  } else if (filters.hotLead === "false") {
    leads = leads.filter((l) => l.hotLead === false)
  }
  if (filters.search) {
    const s = filters.search.toLowerCase()
    leads = leads.filter(
      (l) =>
        l.name.toLowerCase().includes(s) ||
        l.sector.toLowerCase().includes(s) ||
        l.location.toLowerCase().includes(s) ||
        l.area.toLowerCase().includes(s)
    )
  }

  // Attach activities (matching Prisma include shape)
  return leads
    .map((lead) => ({
      ...lead,
      activities: SEED_ACTIVITIES
        .filter((a) => a.leadId === lead.id)
        .slice(0, 3),
    }))
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())
}

export function getFallbackLead(id: string): LeadRecord | null {
  const lead = SEED_LEADS.find((l) => l.id === id)
  if (!lead) return null

  return {
    ...lead,
    activities: SEED_ACTIVITIES.filter((a) => a.leadId === lead.id),
  }
}

export function getFallbackCampaigns(): CampaignRecord[] {
  return [...SEED_CAMPAIGNS].sort(
    (a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime()
  )
}
