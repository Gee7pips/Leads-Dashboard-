const NOW = Date.now()
const DAY = 24 * 60 * 60 * 1000

function iso(daysOffset: number = 0): string {
  return new Date(NOW + daysOffset * DAY).toISOString()
}

// Deterministic CUID-like IDs (stable across calls)
function cuid(seed: number): string {
  const chars = 'abcdefghijklmnopqrstuvwxyz0123456789'
  let id = ''
  for (let i = 0; i < 25; i++) {
    id += chars[(seed * 31 + i * 17) % chars.length]
  }
  return id
}

